const TRANSLATIONS = {
  ar, en, fr, es, de, pt,
  'zh-CN': zhCN,
  'zh-TW': zhCN,
  hi, tr, fa,
  nl, ru,
};

// Merge with English so any missing key gracefully falls back instead of showing undefined
export const getT = (code) => {
  const lang = TRANSLATIONS[code];
  if (!lang) return TRANSLATIONS['en'];
  return { ...TRANSLATIONS['en'], ...lang };
};
